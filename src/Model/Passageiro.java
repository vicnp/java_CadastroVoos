package Model;

/**
 *
 * @author Victor
 */
public class Passageiro {
    private String cpf;
    private String nome;
    private String telefone;
    private String email;

    public Passageiro(String cpf, String nome, String telefone, String email) {
        this.cpf = cpf;
        this.nome = nome;
        this.telefone = telefone;
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    @Override
    public String toString() {
        return "Passageiro{" + "cpf=" + cpf + ", nome=" + nome + ", telefone=" + telefone + ", email=" + email + '}';
    }
    
    
    
}
